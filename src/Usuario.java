public class Usuario {
    private String nome;
    private int idade;
    private String endereco;
    private int rg;
    private String genero;

    public Usuario(String nome, int idade, String endereco, int rg, String genero) {
        this.nome = nome;
        this.idade = idade;
        this.endereco = endereco;
        this.rg = rg;
        this.genero = genero;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public int getRg() {
        return rg;
    }

    public void setRg(int rg) {
        this.rg = rg;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }
}
